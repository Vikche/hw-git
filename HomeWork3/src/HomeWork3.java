public class HomeWork3 {

    public static void main(String[] args) {
        int[] arr = new int[5];
        print1DArray(arr);
    }
    public static void print1DArray(int[] arr) {
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i] + " ");
        }
        System.out.println();
    }

}
